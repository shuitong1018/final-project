//点灯，引脚PA6
void HAL_UARTEx_RxEventCallback(UART_HandleTypeDef *huart, uint16_t Size) {
  if (huart == &huart1) {
    message[Size]='\0';
    blink_mode=0;
    breathe_mode=0;
    HAL_UART_Transmit(&huart1, (uint8_t *)message, Size,HAL_MAX_DELAY);
    if (strncmp(message,"set_mode on",11)==0) {
      HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1);
      __HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_1,150);//重装载是0-255
    }
    else if (strncmp(message,"set_mode off",12)==0) {
      HAL_TIM_PWM_Stop(&htim3, TIM_CHANNEL_1);
    }
    else if (strncmp(message,"set_brightness ",15)==0) {
      HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1);
      int brightness=atoi(message+15);//亮度介于0-255之间
      __HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_1,brightness);
    }
    else if (strncmp(message,"set_mode blink ",15)==0) {
      HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1);
      blink_mode=1;
      blink_frequency=atoi(message+15);
      blink_update=2212/blink_frequency;//算阈值
      }
    else if (strncmp(message,"set_mode breathe ",17)==0) {
      HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1);
      breathe_mode=1;
      breathe_frequency=atoi(message+17);
      breathe_update=2212/breathe_frequency;
    }
    memset(message,0,40);
    HAL_UARTEx_ReceiveToIdle_DMA(&huart1,(uint8_t*)message,40);
    __HAL_DMA_DISABLE_IT(&hdma_usart1_rx,DMA_IT_HT);
  }
}
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {//按频率闪烁，用于计算PWM周期的次数，以便到达阈值后反转小灯状态
  if (htim->Instance == htim3.Instance) {
    if (blink_frequency>0&&blink_mode==1) {
      if (blink_update>0) {
        blink_counter++;
        if (blink_counter>=blink_update) {
          blink_counter=0;
          blink_state=!blink_state;
        }
      }
    }
    if (breathe_mode==1) {
      if (breathe_frequency>0) {
        if (breathe_update>0) {
          breathe_counter++;
          if (breathe_counter>=breathe_update) {
            breathe_counter=0;
            breathe_state=!breathe_state;
          }
          if (breathe_state==1) {
            breathe_compare=breathe_counter*BRIGHTNESS/breathe_update;
          }
          else {
            breathe_compare=1-breathe_counter*BRIGHTNESS/breathe_update;
          }
          __HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_1,breathe_compare);
        }
      }
    }
  }
  if (htim->Instance == htim2.Instance) {//tim2中断处理，ADC测量结果发送频率为72M/72/100000=10Hz，100ms发送一次
    if (value <=4095) {
      voltage = (value/4095)*3.3;
      sprintf(DMA_message,"Voltage: %.2fV",voltage);
      HAL_UART_Transmit(&huart1,(uint8_t *)DMA_message,strlen(DMA_message),HAL_MAX_DELAY);
    }
  }
}

